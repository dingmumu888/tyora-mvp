type CountryEntry = Omit<CountryCallingCode, "flag">;

function countryFlag(iso: string) {
  return iso
    .toUpperCase()
    .split("")
    .map((character) => String.fromCodePoint(127397 + character.charCodeAt(0)))
    .join("");
}

