export const countryCallingCodes: CountryCallingCode[] = entries.map((entry) => ({
  ...entry,
  flag: countryFlag(entry.iso)
}));

export function callingCodeForCountry(countryCode?: string | null) {
  const normalized = countryCode?.trim().toUpperCase();
  return countryCallingCodes.find((country) => country.iso === normalized) || countryCallingCodes[0];
}
